export function buildUserPrompt(request: FortuneReadingRequest): string {
  return [
    "[사용자 질문]",
    request.question,
    "",
    "[MCP가 계산한 검증 사실]",
    request.factContext,
    "",
    "[최종 표현 확인]",
    "- 근거의 위치·시기 1~2개 → 쉬운 뜻 → 구체적인 생활 장면 순서",
    "- 일간과 시주·월주·년주를 같은 위치처럼 합치지 않기",
    "- 십신은 한국어로 쓰고, 계산표 한자 원문·지장간·새 합성 용어는 쓰지 않기",
    "- 종합 질문이면 ### 함께 확인된 근거에 자미두수와 출생차트 근거를 한 문장씩 포함하고 불릿은 쓰지 않기",
    "- '당신은' 없이 자연스러운 존댓말로 완결",
    "- 각 섹션에 업무·돈·관계·결정·휴식 중 관련 있는 구체적 장면을 하나 이상 포함",
  ].join("\n");
}
